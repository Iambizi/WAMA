import { v } from "convex/values";
import { mutation, query, QueryCtx, MutationCtx } from "./_generated/server";
import { requireAdvisor } from "./activityLogs";
import { Id } from "./_generated/dataModel";

// List all matches
export const list = query({
  args: {},
  handler: async (ctx: QueryCtx) => {
    await requireAdvisor(ctx);
    return await ctx.db.query("matches").order("desc").collect();
  },
});

// List all matches populated with buyer and seller details (database join)
export const listPopulated = query({
  args: {},
  handler: async (ctx: QueryCtx) => {
    await requireAdvisor(ctx);
    const matches = await ctx.db.query("matches").order("desc").collect();

    // Populate relational details in a single pass
    const populated = await Promise.all(
      matches.map(async (m) => {
        const buyer = await ctx.db.get(m.buyerId);
        const seller = await ctx.db.get(m.sellerId);
        return {
          ...m,
          buyerName: buyer?.name || "Unknown Buyer",
          buyerEmail: buyer?.email || "",
          sellerBusinessName: seller?.businessName || "Unknown Seller",
          sellerSector: seller?.sector || "",
          sellerRevenueRange: seller?.revenueRange || "under_500k",
        };
      })
    );

    return populated;
  },
});

// List matches for a specific seller mandate
export const listBySeller = query({
  args: { sellerId: v.id("sellers") },
  handler: async (ctx: QueryCtx, args) => {
    await requireAdvisor(ctx);
    return await ctx.db
      .query("matches")
      .withIndex("by_seller", (q) => q.eq("sellerId", args.sellerId))
      .collect();
  },
});

// Get a single match by ID
export const get = query({
  args: { id: v.id("matches") },
  handler: async (ctx: QueryCtx, args) => {
    await requireAdvisor(ctx);
    return await ctx.db.get(args.id);
  },
});

// Upsert a match generated by the Claude matching agent
export const upsertFromAI = mutation({
  args: {
    sellerId: v.id("sellers"),
    buyerId: v.id("buyers"),
    aiScore: v.number(),
    aiReasoning: v.string(),
    aiMatchedCriteria: v.array(v.string()),
  },
  handler: async (
    ctx: MutationCtx,
    args: {
      sellerId: Id<"sellers">;
      buyerId: Id<"buyers">;
      aiScore: number;
      aiReasoning: string;
      aiMatchedCriteria: string[];
    }
  ) => {
    await requireAdvisor(ctx);
    const { sellerId, buyerId, aiScore, aiReasoning, aiMatchedCriteria } = args;
    const now = Date.now();

    // Check if there is an existing match record between the buyer and seller
    const existing = await ctx.db
      .query("matches")
      .withIndex("by_seller", (q) => q.eq("sellerId", sellerId))
      .filter((q) => q.eq(q.field("buyerId"), buyerId))
      .unique();

    if (existing) {
      // Update the AI recommendation parameters of the existing match
      await ctx.db.patch(existing._id, {
        aiScore,
        aiReasoning,
        aiMatchedCriteria,
        updatedAt: now,
      });
      return existing._id;
    } else {
      // Create a brand new match, starting at the "suggested" pipeline stage
      const id = await ctx.db.insert("matches", {
        sellerId,
        buyerId,
        aiScore,
        aiReasoning,
        aiMatchedCriteria,
        status: "suggested",
        createdAt: now,
        updatedAt: now,
      });
      return id;
    }
  },
});

// Log match generation completed event in activityLogs
export const logMatchGeneration = mutation({
  args: {
    sellerId: v.id("sellers"),
    matchCount: v.number(),
  },
  handler: async (
    ctx: MutationCtx,
    args: {
      sellerId: Id<"sellers">;
      matchCount: number;
    }
  ) => {
    await requireAdvisor(ctx);
    const seller = await ctx.db.get(args.sellerId);
    if (!seller) throw new Error("Seller mandate not found");

    const now = Date.now();
    await ctx.db.insert("activityLogs", {
      action: "match_generated",
      entityType: "seller",
      entityId: args.sellerId,
      entityLabel: seller.businessName,
      details: `${args.matchCount} AI match recommendations generated`,
      createdAt: now,
    });
  },
});
